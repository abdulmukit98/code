#include<bits/stdc++.h>
using namespace std;
int main()
{
    /** Series = form i = 1 to n:  i * (n-i+1) = 1*n + 2*(n-1) + .... + n*1 */

    int n; printf("Enter value of n: "); scanf("%d",&n);

    printf("\nSummation From i=1 to %d:  i*(n-i+1) = ", n);

    for(int i = 1; i <= n; i++)
    {
        if(i == 1)
            printf("%d*n + ",i);
        else if (i != n)
            printf("%d*(n-%d) + ", i, i-1);
        else
            printf("n*1 \n\n");

    }
}
